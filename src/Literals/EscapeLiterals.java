package Literals;

public class EscapeLiterals 
{
	public static void main(String[] args) 
	{
		System.out.println("one - two - three - four");
		System.out.println("one \t - two \t - three \t - four");
		System.out.println("one \n - two - three - four");
		System.out.println("one - two - three - four");
		System.out.println("one - two - three - four");
		System.out.println("one - two - three - four");
		System.out.println("one - two - three - four");
	}
}
