package Operator;

public class BitwiseOperator 
{
	public static void main(String[] args)
	{
		 System.out.println(~10);						 //  ~   Bitwise Complement Operator
		 System.out.println(10 | 20);                    //  |   Bitwise OR Operator
		 System.out.println(10 ^ 20);                    //  |   Bitwise Exclusive OR Operator
		 System.out.println(10 & 20);                    //  &   Bitwise AND Operator
		 System.out.println(10 << 20);                   //  <<  Bitwise Left Shift Operator
		 System.out.println(10 >> 20);                   //  <<  Bitwise Right Shift Operator
		 System.out.println(10 >>> 20);                   //  <<  Bitwise Unsigned Right Shift Operator

	}
}
